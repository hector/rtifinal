package rtifinal.graphics;

/*
 Extremely simple class to
 hold each 3D vertex
 */

import processing.core.*;

public class Point3D {

    float x, y, z;
// constructors
    Point3D() {
    }

    Point3D(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
